/** ユーザーのCCで駐輪場をフィルタリング */
export function filterByCC(spots: ParkingPin[], userCC: number): ParkingPin[] {
  return spots.filter((spot) => {
    if (spot.maxCC === null) return true; // 制限なし
    return userCC <= spot.maxCC;
  });
}

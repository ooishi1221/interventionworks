// ─── 駐輪場名テンプレート ────────────────────────────
const NAME_TEMPLATES = [
  '{area}バイク駐輪場',
  '{area}二輪駐車場',
  '{area}バイク駐車場',
  '{area}付近バイク置き場',
  '{area}バイクパーキング',
  '{area}二輪・自転車駐輪場',
  '{area}オートバイ専用駐車場',
  '{area}周辺バイク駐輪場',
];

const SUFFIX = ['東口', '西口', '南口', '北口', '前', '周辺', '地下', '高架下', '第1', '第2', '中央'];

// ─── ユーティリティ ──────────────────────────────────
function rand(min, max) { return min + Math.random() * (max - min); }
function pick(arr) { return arr[Math.floor(Math.random() * arr.length)]; }
function jitter(base, range) { return base + (Math.random() - 0.5) * range; }

function generateId(pref, area, idx) {
  const prefCode = pref.replace(/[都府県]/, '').substring(0, 3);
  const areaCode = area.replace(/[駅 ・（）()]/g, '').substring(0, 6);
  return `${prefCode}_${areaCode}_${String(idx).padStart(3, '0')}`;
}

// CC 分布: 制限なし(40%), 250cc(25%), 125cc(25%), 50cc専用(10%)
function randomMaxCC() {
  const r = Math.random();
  if (r < 0.40) return null;
  if (r < 0.65) return 250;
  if (r < 0.90) return 125;
  return 50;
}

// ─── メイン生成 ──────────────────────────────────────
const spots = [];
let totalCount = 0;

for (const [pref, area, lat, lon, count] of AREAS) {
  for (let i = 0; i < count; i++) {
    totalCount++;
    const suffix = i === 0 ? '' : pick(SUFFIX);
    const nameBase = pick(NAME_TEMPLATES).replace('{area}', area.replace('駅', ''));
    const name = suffix ? `${area}${suffix} ${nameBase.replace(area.replace('駅', ''), '')}`.replace(/  +/g, ' ').trim()
                        : nameBase;

    const maxCC = randomMaxCC();
    const isFree = Math.random() < 0.25; // 25% 無料
    const capacity = Math.round(rand(10, 200));

    spots.push({
      id: generateId(pref, area, i + 1),
      name: name.length > 30 ? `${area}${suffix ? suffix : ''} バイク駐輪場` : name,
      latitude: Number(jitter(lat, 0.006).toFixed(6)),
      longitude: Number(jitter(lon, 0.006).toFixed(6)),
      address: `${pref}${area.replace('駅', '')}付近`,
      maxCC,
      isFree,
      capacity,
      ...(!isFree && { pricePerHour: pick([100, 150, 200, 250, 300, 500]) }),
      source: 'seed',
    });
  }
}

// ─── 出力 ────────────────────────────────────────────
mkdirSync('scripts/data', { recursive: true });
const outPath = 'scripts/data/spots-kanto.json';
writeFileSync(outPath, JSON.stringify(spots, null, 2), 'utf-8');

console.log(`=== データ生成完了 ===`);
console.log(`  エリア数: ${AREAS.length}`);
console.log(`  スポット数: ${spots.length}`);
console.log(`  出力: ${outPath}`);
console.log(`\n都道府県別:`)

const byPref = {};
for (const [pref, , , , count] of AREAS) {
  byPref[pref] = (byPref[pref] || 0) + count;
}
for (const [pref, count] of Object.entries(byPref).sort((a, b) => b[1] - a[1])) {
  console.log(`  ${pref}: ${count}件`);
}

